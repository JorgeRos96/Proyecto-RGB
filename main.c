static void MX_GPIO_Init(void)
{
	GPIO_InitTypeDef GPIO_InitStruct = {0};
  
	/* GPIO Ports Clock Enable */
  __HAL_RCC_GPIOE_CLK_ENABLE();
  __HAL_RCC_GPIOD_CLK_ENABLE();
  __HAL_RCC_GPIOF_CLK_ENABLE();
  __HAL_RCC_GPIOB_CLK_ENABLE();

 
  /*Configure GPIO pins : UP_Pin DOWN_Pin LEFT_Pin RIGHT_Pin
                           CENTER_Pin */
  GPIO_InitStruct.Pin = GPIO_PIN_2|GPIO_PIN_10|GPIO_PIN_5|GPIO_PIN_14
                          |GPIO_PIN_3;
  GPIO_InitStruct.Mode = GPIO_MODE_IT_RISING_FALLING;
  GPIO_InitStruct.Pull = GPIO_PULLDOWN;
  HAL_GPIO_Init(GPIOF, &GPIO_InitStruct);

  

  /* EXTI interrupt init*/
  HAL_NVIC_SetPriority(EXTI2_IRQn, 0, 0);
  HAL_NVIC_EnableIRQ(EXTI2_IRQn);

  HAL_NVIC_SetPriority(EXTI3_IRQn, 0, 0);
  HAL_NVIC_EnableIRQ(EXTI3_IRQn);

  HAL_NVIC_SetPriority(EXTI9_5_IRQn, 0, 0);
  HAL_NVIC_EnableIRQ(EXTI9_5_IRQn);

  HAL_NVIC_SetPriority(EXTI15_10_IRQn, 0, 0);
  HAL_NVIC_EnableIRQ(EXTI15_10_IRQn);


}
/**
  * @brief Función de callback de las lineas de interrupción. En este caso la pulsación del joystick.
	*				 Con la pulsación CENTER se realiza el encendido/apagado del RGB
	*				 Con la pulsación UP y DOWN se amplia o disminuye la intensidad del RGB.
	*				 Con la pulsación LEFT y RIGHT se cambia el color del RGB
	* @param GPIO_Pin: Timer que ha realizado la cuenta e invoca a la función de callback  
  * @retval None
  */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if( GPIO_Pin == GPIO_PIN_2){
		if (inten > 60000)
			inten = 10;
		else
		inten = inten + 30000;
		
if (modo == 0){
		
		 htim1.Instance->CCR2 = inten;
	 }
	 else if (modo == 1){
		 htim1.Instance->CCR3 = inten;
	 }
	 else { 
		 htim4.Instance->CCR4 = inten;
	 }
		
}
if( GPIO_Pin == GPIO_PIN_3){
		if (inten < 2000)
			inten = 60010;
		else
		inten = inten - 30000;
		
if (modo == 0){
		
		 htim1.Instance->CCR2 = inten;
	 }
	 else if (modo == 1){
		 htim1.Instance->CCR3 = inten;
	 }
	 else { 
		 htim4.Instance->CCR4 = inten;
	 }
}
if( GPIO_Pin == GPIO_PIN_5){
	 	 if (modo == 0){
		 HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_4);
		 HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_2);
		 htim1.Instance->CCR2 = inten;
			 modo = 2;
	 }
	 else if (modo == 1){
		 HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
		 HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_3);
		 htim1.Instance->CCR3 = inten;
		 modo = 0;
	 }
	 else {
		 HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);
		 HAL_TIM_PWM_Stop(&htim4, TIM_CHANNEL_4);
		 htim4.Instance->CCR4 = inten;
		 modo = 1;
	 }
}
if( GPIO_Pin == GPIO_PIN_10){
	 if (modo == 0){
		 HAL_TIM_PWM_Stop(&htim4, TIM_CHANNEL_4);
		 HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
		 htim1.Instance->CCR2 = inten;
			 modo = 1;
	 }
	 else if (modo == 1){
		 HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_2);
		 HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);
		 htim1.Instance->CCR3 = inten;
		 modo = 2;
	 }
	 else {
		 HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_3);
		 HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_4);
		 htim4.Instance->CCR4 = inten;
		 modo = 0;
	 }
}
if( GPIO_Pin == GPIO_PIN_14){
	 if (encender == 0){
		 encender = 1;
		 HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_4);
		 htim4.Instance->CCR4 = inten;
	 }
	 else {
		 encender = 0;
		 HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_2);
		 HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_3);
		 HAL_TIM_PWM_Stop(&htim4, TIM_CHANNEL_4);
	 }
		
}
}
